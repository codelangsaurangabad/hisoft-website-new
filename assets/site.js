function translatePlainText(lang) {
  const shouldSkip = (node) => {
    const parent = node.parentElement;
    return !parent || ["SCRIPT", "STYLE", "NOSCRIPT"].includes(parent.tagName) || parent.closest("[data-no-auto-translate], [data-en][data-mr]");
  };
  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      if (shouldSkip(node) || !node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    }
  });
  const textNodes = [];
  while (walker.nextNode()) textNodes.push(walker.currentNode);
  textNodes.forEach((node) => {
    if (!node._enAutoText) node._enAutoText = node.nodeValue;
    const english = node._enAutoText;
    const key = english.trim().replace(/\s+/g, " ");
    if (lang === "mr" && marathiPhrases[key]) {
      node.nodeValue = english.replace(key, marathiPhrases[key]);
    } else if (lang === "en") {
      node.nodeValue = english;
    }
  });
  document.querySelectorAll("[placeholder]").forEach((node) => {
    if (!node.dataset.enAutoPlaceholder) node.dataset.enAutoPlaceholder = node.getAttribute("placeholder");
    const english = node.dataset.enAutoPlaceholder;
    node.setAttribute("placeholder", lang === "mr" && marathiPhrases[english] ? marathiPhrases[english] : english);
  });
  document.querySelectorAll("[title]").forEach((node) => {
    if (!node.dataset.enAutoTitle) node.dataset.enAutoTitle = node.getAttribute("title");
    const english = node.dataset.enAutoTitle;
    node.setAttribute("title", lang === "mr" && marathiPhrases[english] ? marathiPhrases[english] : english);
  });
}

function applyLanguage(lang) {
  document.documentElement.lang = lang === "mr" ? "mr" : "en";
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (translations[lang]?.[key]) node.textContent = translations[lang][key];
  });
  document.querySelectorAll("[data-en][data-mr]").forEach((node) => {
    node.textContent = node.getAttribute(lang === "mr" ? "data-mr" : "data-en");
  });
  translatePlainText(lang);
  localStorage.setItem("hisoft-lang", lang);
  document.querySelectorAll("[data-lang-set]").forEach((link) => {
    const isActive = link.getAttribute("data-lang-set") === lang;
    if (isActive) link.setAttribute("aria-current", "true");
    else link.removeAttribute("aria-current");
  });
  updateDynamicHeroWords(lang);
}

function updateDynamicHeroWords(lang) {
  document.querySelectorAll(".dynamic-hero-title").forEach((title) => {
    const staticNode = title.querySelector("[data-dynamic-static]");
    const wordNode = title.querySelector("[data-dynamic-word]");
    const words = (title.getAttribute(lang === "mr" ? "data-mr-words" : "data-en-words") || "")
      .split(",")
      .map((word) => word.trim())
      .filter(Boolean);
    if (staticNode) staticNode.textContent = title.getAttribute(lang === "mr" ? "data-mr-static" : "data-en-static") || "";
    if (wordNode && words.length) {
      title._dynamicWords = words;
      title._dynamicIndex = Math.min(title._dynamicIndex || 0, words.length - 1);
      wordNode.textContent = words[title._dynamicIndex];
    }
  });
}

function initDynamicHeroWords() {
  document.querySelectorAll(".dynamic-hero-title").forEach((title) => {
    const wordNode = title.querySelector("[data-dynamic-word]");
    if (!wordNode) return;
    title._dynamicIndex = 0;
    window.setInterval(() => {
      const words = title._dynamicWords || [];
      if (words.length < 2) return;
      title._dynamicIndex = (title._dynamicIndex + 1) % words.length;
      const effect = title._dynamicIndex % 3;
      wordNode.classList.remove("swap-slide", "swap-scale", "swap-blur");
      wordNode.classList.add("is-leaving");
      window.setTimeout(() => {
        wordNode.textContent = words[title._dynamicIndex];
        wordNode.classList.remove("is-leaving");
        wordNode.classList.add(effect === 0 ? "swap-slide" : effect === 1 ? "swap-scale" : "swap-blur");
      }, 260);
    }, 2200);
  });
}

